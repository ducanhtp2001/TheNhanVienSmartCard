    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource().equals(btnthongtin)) {
           thongtinClick();
        }else if (e.getSource().equals(btnchamcong)) {
           chamcongClick();
        }else if (e.getSource().equals(btnravao)) {
           ravaoClick();
        }else if (e.getSource().equals(btnquanlixe)) {
           quanlixeClick();
        }else  {
           backClick();
        }
    }
