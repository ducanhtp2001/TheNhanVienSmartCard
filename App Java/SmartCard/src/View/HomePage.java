    public void chamcongClick() {
    Timestamp currentTimestamp;
        currentTimestamp = new Timestamp(System.currentTimeMillis());
        if(ConnectDatabase.chamCong == 0) {
            JOptionPane.showMessageDialog(this, "Bắt đầu làm việc: " + currentTimestamp);
        } else {
            JOptionPane.showMessageDialog(this, "Dừng làm việc: " + currentTimestamp);
        }
        ConnectDatabase.chamCong(Integer.parseInt(nhanVien.getId()));
        System.out.println(Integer.parseInt(nhanVien.getId()));
}

