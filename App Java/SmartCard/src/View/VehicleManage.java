    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        selectIndex = jTableXe.getSelectedRow();
        int id = ConnectDatabase.lstXe.get(selectIndex).getId();
        String loaiXe = null;
        String bienSo = null;
        EditDialog dialog = new EditDialog(this);
                boolean isOkClicked = dialog.showDialog();
                if (isOkClicked) {
                    // Lấy thông tin đã sửa
                    loaiXe = dialog.getSelectedOption();
                    bienSo = dialog.getTextValue();

                    // Cập nhật thông tin trong bảng
                    System.out.println("loaiXe: " + loaiXe);
                    System.out.println("bienSo: " + bienSo);
                }
        ConnectDatabase.updateXe(id, loaiXe, bienSo);
        showListXe();
    }//GEN-LAST:event_jButton2ActionPerformed

