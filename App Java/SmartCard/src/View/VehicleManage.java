    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        selectIndex = jTableXe.getSelectedRow();
        Xe xe = ConnectDatabase.lstXe.get(selectIndex);
    
        Timestamp currentTimestamp = new Timestamp(System.currentTimeMillis());
     
        if(ConnectDatabase.guiXe == 0) {
            JOptionPane.showMessageDialog(this, "Gửi xe "+ xe.getLoaiXe() + " có biển số: " + xe.getBienSo() + " vào lúc: " + currentTimestamp);
        } else {
            JOptionPane.showMessageDialog(this, "Lấy xe "+ xe.getLoaiXe() + " có biển số: " + xe.getBienSo() + " vào lúc: " + currentTimestamp);

        }
        ConnectDatabase.guiXe(xe);
    }//GEN-LAST:event_jButton4ActionPerformed

